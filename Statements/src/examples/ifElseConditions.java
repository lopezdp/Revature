package examples;

public class ifElseConditions {
	
	public static void main(String[] args) {
		int num = 4;
		if(num % 2 == 0) {
			System.out.println("The number " + num + " is even.");
		} else {
			System.out.println("The number " + num + " is odd.");
		}
		System.exit(0);
	}

}
